package com.zhisland.android.blog.profile.eb;

import com.zhisland.android.blog.profile.dto.Company;

public class EBCompany {

	public static final int TYPE_COMPANY_EDIT = 1;
	public static final int TYPE_COMPANY_ADD = 2;
	public static final int TYPE_COMPANY_DELETE = 3;
	
	private int type;
	private Company company;

	public EBCompany(int type, Company company) {
		this.type = type;
		this.company = company;
	}

	public int getType() {
		return type;
	}

	public Company getCompany() {
		return company;
	}
}
